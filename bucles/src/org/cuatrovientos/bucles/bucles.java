package org.cuatrovientos.bucles;

public class bucles {
	public static void main(String[] arg)  {
		int number = 10;
	
		while (number > 0) {
			System.out.println("LOL");
			number = number - 1;
		}
		
	do {
		System.out.println("LOL");
		number = number - 1;
	} while (number < 10);
	
	if (number > 0 ) {
	for (int i = 0; i<10 ; i++) {
		System.out.println(i + "Hello");
		}
	} else { 
		System.out.println("Invalid number");
	
	}
	}
}
